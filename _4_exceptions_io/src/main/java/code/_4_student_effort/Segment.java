package code._4_student_effort;

public class Segment {
    private boolean a;
    private boolean b;
    private boolean c;
    private boolean d;
    private boolean e;
    private boolean f;
    private boolean g;

    public Segment(boolean a, boolean b, boolean c, boolean d, boolean e, boolean f, boolean g) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
        this.e = e;
        this.f = f;
        this.g = g;
    }

    public boolean isA() {
        return a;
    }

    public boolean isB() {
        return b;
    }

    public boolean isC() {
        return c;
    }

    public boolean isD() {
        return d;
    }

    public boolean isE() {
        return e;
    }

    public boolean isF() {
        return f;
    }

    public boolean isG() {
        return g;
    }
}
